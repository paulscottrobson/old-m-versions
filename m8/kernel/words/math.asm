; ========================================================================================================
;												Multiply
; ========================================================================================================

; @word *
		jp 		Multiply16

; ========================================================================================================
;												Divide
; ========================================================================================================

; @word /

		push 		de 							; save DE (B)
		call 		DivideMod16 				; DE = result, HL = mod
		ex 			de,hl 						; result in HL
		pop 		de 							; restore DE
		ret

; ========================================================================================================
;												Modulus
; ========================================================================================================

; @word mod

		push 		de 							; save DE (B)
		call 		DivideMod16 				; DE = result, HL = mod
		pop 		de 							; restore DE
		ret
		
